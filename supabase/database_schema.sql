-- 1. TABLA DE NEGOCIOS (EMPRESAS)
CREATE TABLE negocios (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre TEXT NOT NULL,
  stripe_customer_id TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. TABLA DE PLANES
CREATE TABLE planes (
  id INT PRIMARY KEY,
  codigo TEXT UNIQUE NOT NULL, -- 'free', 'basic', 'premium'
  nombre TEXT NOT NULL,
  precio_mxn NUMERIC NOT NULL,
  limite_sucursales INT NOT NULL,
  limite_empleados_sucursal INT NOT NULL,
  descripcion TEXT,
  activo BOOLEAN DEFAULT TRUE
);

